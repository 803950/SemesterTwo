
/**
 * An example of using encapsulation in a class
 *
 * @author Tal Dickman
 * @version 1/18/20
 */

public class Student
{
    private String name;
    private int age;
    public Student(){
        
    }
    public Student(String name, int age){
        this.name = name;
        this.age = age;
    }
}

